#!/usr/bin/env node
// Walk docs/**/*.md, parse YAML frontmatter, and emit:
//   - src/web/generated/help-topics.json — { [topicId]: { title, summary, href } }
//   - src/web/generated/help-topics.d.ts  — typed topic-id union
//
// A topic entry is:
//   ---
//   title: "Reviewer mode"
//   help_topic: lever.reviewer_mode
//   help_summary: |
//     Controls whether review is done by an LLM, by deterministic checks,
//     or LLM-first with deterministic fallback.
//   ---
//
// The "summary" is the short prose surfaced in the UI tooltip/modal.
// The "href" points to the same page on the published docs site.

import { readFile, writeFile, mkdir, readdir } from 'node:fs/promises'
import { join, relative, dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import yaml from 'js-yaml'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const DOCS_DIR = resolve(ROOT, 'docs')
const OUT_DIR = resolve(ROOT, 'src/web/generated')
const OUT_JSON = join(OUT_DIR, 'help-topics.json')
const OUT_DTS = join(OUT_DIR, 'help-topics.d.ts')
const DOCS_BASE_URL = '/guildhall/'

const FRONTMATTER_RE = /^---\r?\n([\s\S]*?)\r?\n---\r?\n?/

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true })
  const files = []
  for (const entry of entries) {
    if (entry.name.startsWith('.')) continue
    if (entry.name === 'node_modules') continue
    const full = join(dir, entry.name)
    if (entry.isDirectory()) files.push(...(await walk(full)))
    else if (entry.isFile() && entry.name.endsWith('.md')) files.push(full)
  }
  return files
}

function pathToHref(relPath) {
  const noExt = relPath.replace(/\.md$/, '')
  const clean = noExt.endsWith('/index') ? noExt.slice(0, -'/index'.length) + '/' : noExt
  return DOCS_BASE_URL + clean
}

function parseFrontmatter(content, file) {
  const match = content.match(FRONTMATTER_RE)
  if (!match) return null
  try {
    const data = yaml.load(match[1])
    if (data && typeof data === 'object') return data
  } catch (err) {
    throw new Error(`Malformed YAML frontmatter in ${file}: ${err.message}`)
  }
  return null
}

async function main() {
  const files = await walk(DOCS_DIR)
  const topics = {}
  const collisions = []

  for (const abs of files) {
    const rel = relative(DOCS_DIR, abs)
    const raw = await readFile(abs, 'utf8')
    const fm = parseFrontmatter(raw, rel)
    if (!fm || !fm.help_topic) continue

    const id = String(fm.help_topic).trim()
    if (!/^[a-z0-9][a-z0-9._-]*$/.test(id)) {
      throw new Error(`Invalid help_topic id "${id}" in ${rel} — use lowercase letters, digits, dot, dash, underscore.`)
    }
    if (topics[id]) {
      collisions.push({ id, files: [topics[id].source, rel] })
      continue
    }
    const summary = typeof fm.help_summary === 'string' ? fm.help_summary.trim() : ''
    if (!summary) {
      throw new Error(`help_topic ${id} (${rel}) has no help_summary — add a help_summary: field with a short description.`)
    }
    topics[id] = {
      title: typeof fm.title === 'string' ? fm.title : id,
      summary,
      href: pathToHref(rel),
      source: rel,
    }
  }

  if (collisions.length) {
    const msg = collisions
      .map(({ id, files }) => `  - ${id}: ${files.join(' ↔ ')}`)
      .join('\n')
    throw new Error(`Duplicate help_topic ids:\n${msg}`)
  }

  const outShape = Object.fromEntries(
    Object.entries(topics)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([id, v]) => [id, { title: v.title, summary: v.summary, href: v.href }]),
  )

  await mkdir(OUT_DIR, { recursive: true })
  await writeFile(OUT_JSON, JSON.stringify(outShape, null, 2) + '\n', 'utf8')

  const ids = Object.keys(outShape).sort()
  const union = ids.length ? ids.map((id) => `  | '${id}'`).join('\n') : "  | never"
  const dts = `// AUTO-GENERATED by scripts/extract-help-topics.mjs — do not edit by hand.
export type HelpTopicId =
${union};

export interface HelpTopicEntry {
  title: string
  summary: string
  href: string
}

declare const topics: Record<HelpTopicId, HelpTopicEntry>
export default topics
`
  await writeFile(OUT_DTS, dts, 'utf8')

  console.log(`help-topics: wrote ${ids.length} topic(s) → ${relative(ROOT, OUT_JSON)}`)
}

main().catch((err) => {
  console.error('help-topics: extraction failed')
  console.error(err.message)
  process.exit(1)
})
